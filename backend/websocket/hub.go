package websocket

import (
	"fmt"
	"sync"
)

// WebSocket hub to manage connections
type Hub struct {
	clients    map[*Client]bool
	broadcast  chan []byte
	register   chan *Client
	unregister chan *Client
	mutex      sync.RWMutex
}

// NewHub creates a new WebSocket hub
func NewHub() *Hub {
	return &Hub{
		clients:    make(map[*Client]bool),
		broadcast:  make(chan []byte),
		register:   make(chan *Client),
		unregister: make(chan *Client),
	}
}

// Run starts the hub's main loop
func (h *Hub) Run() {
	for {
		select {
		case client := <-h.register:
			h.mutex.Lock()
			h.clients[client] = true
			h.mutex.Unlock()
			fmt.Printf("WebSocket client connected. Total clients: %d\n", len(h.clients))
			
		case client := <-h.unregister:
			h.mutex.Lock()
			if _, ok := h.clients[client]; ok {
				delete(h.clients, client)
				close(client.send)
			}
			h.mutex.Unlock()
			fmt.Printf("WebSocket client disconnected. Total clients: %d\n", len(h.clients))
			
		case message := <-h.broadcast:
			h.mutex.RLock()
			for client := range h.clients {
				select {
				case client.send <- message:
				default:
					delete(h.clients, client)
					close(client.send)
				}
			}
			h.mutex.RUnlock()
		}
	}
}

// GetBroadcastChannel returns the broadcast channel
func (h *Hub) GetBroadcastChannel() chan []byte {
	return h.broadcast
}

// GetRegisterChannel returns the register channel
func (h *Hub) GetRegisterChannel() chan *Client {
	return h.register
} 